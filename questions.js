function parseCsvLine(line) {
  const fields = [];
  let current = "";
  let inQuotes = false;

  for (let i = 0; i < line.length; i++) {
    const ch = line[i];

    if (ch === "\"") {
  if (inQuotes && line[i + 1] === "\"") {
    current += "\"";

        i++;
      } else {
        inQuotes = !inQuotes;
      }
    } else if (ch === "," && !inQuotes) {
      fields.push(current);
      current = "";
    } else {
      current += ch;
    }
  }
  fields.push(current);
  return fields;
}

function parseQuestions(csv) {
  const lines = csv.split(/\r?\n/);
  return lines.map((line) => {
    const [categoryId, category, difficulty, extra, question, answer] =
      parseCsvLine(line);

    return {
      categoryId: Number(categoryId),
      category,
      difficulty: difficulty ? Number(difficulty) : null,
      extra: extra ? Number(extra) : null,
      question,
      answer,
    };
  });
}

